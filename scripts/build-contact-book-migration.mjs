import { mkdir, readFile, writeFile } from 'node:fs/promises'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const scriptDirectory = dirname(fileURLToPath(import.meta.url))
const projectDirectory = resolve(scriptDirectory, '..')
const migrationPath = resolve(
  projectDirectory,
  'supabase/migrations/20260719090000_contact_book_initial.sql',
)

const sources = [
  {
    label: '共用英文單字系統的 Auth 相容設定',
    path: 'supabase/shared-project-compatibility.sql',
  },
  {
    label: '線上聯絡簿資料庫結構',
    path: 'supabase/schema.sql',
  },
  {
    label: '線上聯絡簿資料存取政策',
    path: 'supabase/rls-policies.sql',
  },
]

const sections = await Promise.all(
  sources.map(async ({ label, path }) => {
    const sql = await readFile(resolve(projectDirectory, path), 'utf8')
    return [`-- ${label}`, `-- Source: ${path}`, sql.trim()].join('\n')
  }),
)

const migration = [
  '-- Generated by scripts/build-contact-book-migration.mjs.',
  '-- Do not include seed.sql: production class data is imported separately.',
  'begin;',
  ...sections,
  'commit;',
  '',
].join('\n\n')

await mkdir(dirname(migrationPath), { recursive: true })
await writeFile(migrationPath, migration, 'utf8')

console.log(`Built ${migrationPath}`)
